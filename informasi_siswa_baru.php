<h3>Selamat Datang Calon Peserta Pendidikan Tahun 2017</h3> <br>
<p>Sebelumnya perlu kami informasikan bahwa PSPP (Pendidikan Staff Penerbangan dan Pramugari) membuka pendaftaran pada tahun 2017 dengan angkatan sebagai berikut :<br><br>
1. Kelas Februari 2017<br>
2. Kelas Juli 2017<br>
3. Kelas Oktober 2017<br>
<br>Saat ini kami masih membuka pendaftaran tersebut dengan ketentuan sebagai berikut :<br>
1. Biaya pendaftaran Rp.300.000,-<br>
2. Biaya pendidikan klik <a href="index.php?id=92&read=biaya-pendidikan-kampus-pspp-integrated.html">disini</a><br>
3. Pembayaran biaya pendidikan hanya kami layani di satu-satunya nomor rekening kami Bank BRI 0098-01-002137-30-3 an PSPP Penerbangan<br>
4. PSPP tidak bertanggungjawab jika anda melakukan pembayaran diluar ketentuan point 3.<br>
5. Quota per kampus yang kami sediakan adalah 150 kursi pendidikan per angkatan yang terdiri dari 3 jurusan pendidikan (AVSEC, STAFF AIRLINES dan PRAMUGARI)<br>
6. Pendaftaran per angkatan kami tutup sewaktu-waktu apabila quota pada angkatan tersebut sudah terpenuhi. <br><br>
<strong>Kapan pendidikan dilaksanakan?</strong><br><br>
1. Pendidikan Kelas Februari 2017 akan dilaksanakan pada bulan februari 2017 (jadwal dipublish 2 minggu sebelum pendidikan dilaksanakan) dan kelas dilaksanakan apabila quota kelas februari terpenuhi. Apabila quota kelas februari tidak terpebuhi maka peserta pendidikan kelas februari 2017 dialihkan ke angkatan berikutnya yaitu kelas Juli 2017.<br><br>
2. Pendidikan Kelas Juli 2017 akan dilaksanakan pada bulan Juli 2017. Jadwal Pendidikan PSPP Penerbangan Kelas Juli 2017 bisa <a href="http://pspp-integrated.com/index.php?id=464&grup=berita&read=jadwal-pra-pendidikan-kelas-Juli-2017.html">diakses disini</a><br><br><br>
3. Pendidikan Kelas Oktober 2017 akan dilaksanakan pada bulan oktober 2017. Jadwal Pendidikan PSPP Penerbangan Kelas Oktober 2017 bisa <a href="http://pspp-integrated.com/index.php?id=465&grup=berita&read=jadwal-pra-pendidikan-kelas-oktober-2017.html">diakses disini</a><br>

